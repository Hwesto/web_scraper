def _country_fig(name, code, ex, im, primary, bil, named) -> str:
    """Adaptive card figure: dual-flow volume · labelled price · dual partner graph ·
    named-exporter depth (where it exists). Panels render only when they have data."""
    panels = ["vol", "price"]
    Y = int(bil["year"].max()) if len(bil) else None
    dest = (bil[(bil["exporter_code"] == code) & (bil["year"] == Y)]
            .groupby("importer")["value_usd"].sum().nlargest(4)) if code else pd.Series(dtype=float)
    orig = (bil[(bil["importer_code"] == code) & (bil["year"] == Y)]
            .groupby("exporter")["value_usd"].sum().nlargest(4)) if code else pd.Series(dtype=float)
    if len(dest) or len(orig):
        panels.append("partners")
    if named is not None and len(named):
        panels.append("named")
    n = len(panels)
    fig, axs = plt.subplots(1, n, figsize=(2.8 * n, 2.15))
    axs = np.atleast_1d(axs)
    for ax, p in zip(axs, panels):
        if p == "vol":
            series = [s for s in (("exports", ex, ACCENT), ("imports", im, "#6a8caf")) if len(s[1])]
            for label, d, col in series:
                vy = (d["net_kg"] / 1e6).where(d["net_kg"] > 0)     # mask quantity-gap years
                ax.plot(d["year"], vy, color=col, lw=1.8, label=label, zorder=4)
            if len(series) == 2:                                    # fill the SMALLER flow
                small = min(series, key=lambda s: s[1]["net_kg"].iloc[-1])
                ax.fill_between(small[1]["year"], (small[1]["net_kg"] / 1e6).where(small[1]["net_kg"] > 0),
                                color=small[2], alpha=0.32, zorder=2)
                ax.legend(fontsize=7, frameon=False, loc="upper left", handlelength=1.0)
            elif series:
                d, col = series[0][1], series[0][2]
                ax.fill_between(d["year"], (d["net_kg"] / 1e6).where(d["net_kg"] > 0),
                                color=col, alpha=0.85, zorder=2)
            yrs = sorted(set(ex["year"].tolist() + im["year"].tolist()))
            ax.set_xticks([yrs[0], yrs[-1]] if yrs else []); ax.set_ylim(bottom=0)
            ax.set_title("volume · kt", fontsize=9.5, color=SUBTLE, loc="left")
        elif p == "price":
            d = ex if primary == "exporter" else im
            py = d["unit_usd_kg"].where(d["net_kg"] > 0)            # $/kg is meaningless where qty=0
            ax.plot(d["year"], py, color="#b8860b", lw=2.0, marker="o", ms=3, zorder=4)
            ax.set_xticks([int(d["year"].min()), int(d["year"].max())])
            ax.set_title(f"avg $/kg · customs {'FOB' if primary=='exporter' else 'CIF'}",
                         fontsize=9.5, color=SUBTLE, loc="left")
        elif p == "partners":
            labels, vals, cols = [], [], []
            for c, v in dest.items():
                labels.append(f"▸ {c}"); vals.append(v / 1e6); cols.append(ACCENT)
            for c, v in orig.items():
                labels.append(f"◂ {c}"); vals.append(v / 1e6); cols.append("#6a8caf")
            yy = list(range(len(labels)))
            ax.barh(yy, vals, color=cols, height=0.72)
            ax.set_yticks(yy); ax.set_yticklabels(labels, fontsize=7.5); ax.invert_yaxis()
            ax.set_title("sells to ▸ / buys from ◂ · $M", fontsize=8.8, color=SUBTLE, loc="left")
        elif p == "named":
            t = named.head(6)
            yy = list(range(len(t)))
            ax.barh(yy, t["volume_t"] / 1000, color="#7a5c8e", height=0.72)
            ax.set_yticks(yy); ax.set_yticklabels(t["company"], fontsize=7.5); ax.invert_yaxis()
            ax.set_title("named exporters · kt", fontsize=8.8, color=SUBTLE, loc="left")
        _bare(ax, grid="y" if p in ("vol", "price") else "x")
        ax.tick_params(labelsize=8.2)
    return _png(fig)


def _extra_facts(name, code, iso3, fa_iso, wx) -> list[str]:
    """Adaptive 'small facts' — only the ones that exist for this country."""
    out = []
    try:                                                  # the real-time campaign layer leads
        if name == "South Africa":                        # live-parsed weekly PDF
            from atlas import berriesza
            h = berriesza.headline()
            if h:
                out.append(f'<b style="color:#2f6f4e">current season — {h}</b>')
                reg = berriesza.load()
                reg = reg[reg["region"] != "Total"].sort_values("total_t", ascending=False).head(3)
                if len(reg):
                    out.append("by region: " + ", ".join(f"{r['region']} {r['total_t']/1000:.0f}kt"
                                                          for _, r in reg.iterrows()))
        else:
            from atlas import campaigns
            h = campaigns.headline(name)
            if h:
                out.append(f'<b style="color:#2f6f4e">current season — {h}</b>')
    except Exception:
        pass
    produced = False
    if iso3 is not None:
        f = fa_iso[fa_iso["iso3"] == iso3]
        if len(f):
            fl = f.sort_values("year").iloc[-1]
            out.append(f"produces <b>{fl['production_t']/1000:.0f} kt</b> ({fl['yield_t_ha']:.1f} t/ha)")
            produced = True
    if not produced and name != "China":                  # FAOSTAT-missing producers (SA, Argentina)
        try:
            from atlas import production
            p = production.latest(name, "production")
            if p:
                out.append(f"produces ~<b>{p/1000:.0f} kt</b> (snapshot — FAOSTAT-missing)")
        except Exception:
            pass
    try:
        from atlas import usda_gain
        fc = usda_gain.load()
        fc = fc[(fc["country"] == name) & (fc["metric"] == "production")]
        if len(fc):
            out.append(f"forecast <b>{fc.iloc[0]['value_mt']/1000:.0f} kt</b> ({int(fc.iloc[0]['year'])})")
    except Exception:
        pass
    w = wx[wx["origin"] == name] if not wx.empty else wx
    if not w.empty:
        cold = w.groupby("month")["tmin"].mean().min()
        out.append("frost-free" if cold > 1 else f"frost risk (min {cold:.0f}°C)")
    if name == "China":                                  # the gated bloc, as a press snapshot
        try:
            from atlas import china
            h = china.headline()
            if h:
                out.append(f'<b style="color:#2f6f4e">{h}</b>')
        except Exception:
            pass
    if name == "USA":                                     # unique AMS view: US supply by origin
        try:
            from atlas import usda_movement
            mv = usda_movement.load()
            if len(mv):
                rd = str(mv["report_date"].iloc[0])[:7]
                top = mv.sort_values("season_t", ascending=False).head(4)
                bits = ", ".join(f"{r['origin']} {r['season_t']/1000:.0f}kt" for _, r in top.iterrows())
                out.append(f"US supply by origin (AMS to {rd}): {bits}")
        except Exception:
            pass
    try:
        from atlas import senasica
        o = senasica.load(fruit="Arándano")
        if name == "Mexico" and len(o):
            out.append(f"<b>{len(o)}</b> named export orchards")
    except Exception:
        pass
    return out


def _slug(name: str) -> str:
    return "c-" + re.sub(r"[^a-z0-9]", "", name.lower())


def _country_card(name, code, cname, iso3, reg, fa_iso, wx, bil) -> str:
    sub = cs.load()
    sub = sub[sub["reporter_code"] == code] if code else sub.iloc[0:0]
    ex = sub[sub["role"] == "exporter"].sort_values("year")
    im = sub[sub["role"] == "importer"].sort_values("year")
    lv = lambda d: float(d["value_usd"].iloc[-1]) if len(d) else 0.0
