def _relay_png() -> str:
    m = cmth.load()
    if m.empty:
        return ""
    recent = m[m["year"] >= m["year"].max() - 2]
    top = (recent.groupby("exporter")["net_kg"].sum()
           .sort_values(ascending=False).head(6).index.tolist())
    rel = (recent[recent["exporter"].isin(top)]
           .groupby(["month", "exporter"])["net_kg"].sum().unstack(fill_value=0))
    rel = rel.reindex(columns=top).reindex(range(1, 13)).fillna(0)
    share = rel.div(rel.sum(axis=1).replace(0, np.nan), axis=0).fillna(0) * 100
    fig, ax = plt.subplots(figsize=(9.2, 4.0))
    ax.stackplot(range(1, 13), *[share[c] for c in top], labels=top,
                 colors=ORIGIN_COLORS[:len(top)], alpha=0.92)
    ax.set_xlim(1, 12); ax.set_ylim(0, 100)
    ax.set_xticks(range(1, 13))
    ax.set_xticklabels(["J", "F", "M", "A", "M", "J", "J", "A", "S", "O", "N", "D"])
    ax.set_yticks([0, 25, 50, 75, 100]); ax.set_yticklabels(["0", "25", "50", "75", "100%"])
    _bare(ax, grid="y")
    ax.legend(loc="upper center", bbox_to_anchor=(0.5, -0.08), ncol=len(top),
              frameon=False, fontsize=10, handlelength=1.0, columnspacing=1.2)
    return _png(fig)


def _ladder_png(role="exporter") -> str:
    yr = _latest_final(role)
    df = cs.ranking(role, yr).head(10).iloc[::-1]
    prev = cs.ranking(role, yr - 1)
    pmap = dict(zip(prev["country"], prev["share"])) if len(prev) else {}
    if df.empty:
        return ""
    fig, ax = plt.subplots(figsize=(4.5, 4.0))
    y = range(len(df))
    ax.barh(y, df["value_usd"] / 1e6, color=ACCENT if role == "exporter" else "#6a8caf",
            height=0.66, zorder=3)
    ax.set_yticks(y); ax.set_yticklabels(df["country"], fontsize=10)
    for i, (_, r) in enumerate(df.iterrows()):
        d = (r["share"] - pmap.get(r["country"], r["share"])) * 100
        arr = "▲" if d > 0.05 else ("▼" if d < -0.05 else "·")
        uv = f" ${r['unit_usd_kg']:.1f}" if r["unit_usd_kg"] > 0 else ""
        ax.text(r["value_usd"] / 1e6, i, f"  {r['share']*100:.0f}% {arr}{uv}",
                va="center", ha="left", fontsize=9, color=SUBTLE)
    ax.set_xlim(0, df["value_usd"].max() / 1e6 * 1.35)
    ax.xaxis.set_major_formatter(FuncFormatter(lambda v, _: f"${v/1000:.1f}B" if v >= 1000 else f"${v:.0f}M"))
    _bare(ax, grid="x")
    return _png(fig)


def _divergence_png() -> str:
    yr = _latest_final("exporter")
    ex = cs.ranking("exporter", yr)[["reporter_code", "country", "value_usd"]]
    c2i = _code_to_iso3()
    ex["iso3"] = ex["reporter_code"].map(c2i)
    fa = faostat.load()
    if fa.empty or ex.empty:
        return ""
    fy = int(fa["year"].max())
    fa = fa[fa["year"] == fy].copy()
    m2i = _faostat_m49_to_iso3()
    fa["iso3"] = fa["m49"].map(m2i)
    j = ex.merge(fa[["iso3", "production_t"]], on="iso3", how="inner")
    j = j[(j["production_t"] > 0) & (j["value_usd"] > 0)]
    if j.empty:
        return ""
    fig, ax = plt.subplots(figsize=(5.2, 4.2))
    ax.scatter(j["production_t"] / 1000, j["value_usd"] / 1e6, s=46, color=ACCENT,
               alpha=0.8, zorder=3, edgecolor="white", linewidth=0.6)
    for _, r in j.iterrows():
        if r["value_usd"] > 1.5e8 or r["production_t"] > 5e4:
            ax.annotate(r["country"], (r["production_t"] / 1000, r["value_usd"] / 1e6),
                        fontsize=8.5, color=INK, xytext=(4, 2), textcoords="offset points")
    # China -- the world #1 producer FAOSTAT misses: grows 810kt, exports ~nothing
    try:
        from atlas import china
        cp = china.latest("production", "China total")
        ce = ex[ex["country"] == "China"]["value_usd"]
        if cp and len(ce):
            ax.scatter([cp / 1000], [ce.iloc[0] / 1e6], s=80, color="#9c4221", zorder=5,
                       edgecolor="white", linewidth=0.7)
            ax.annotate("China (#1 grower, barely exports)", (cp / 1000, ce.iloc[0] / 1e6),
                        fontsize=8.5, color="#9c4221", fontweight="bold",
                        xytext=(-150, 6), textcoords="offset points")
    except Exception:
        pass
    ax.set_xlabel("production  (000 t · FAOSTAT + China snapshot)"); ax.set_ylabel("exports  ($M, Comtrade)")
    ax.set_xscale("log"); ax.set_yscale("log")
    _bare(ax, grid="both")
    return _png(fig)


def _choropleth_png() -> str:
    if not GEOJSON.exists():
        return ""
    yr = _latest_final("exporter")
    ex = cs.ranking("exporter", yr)
    c2i = _code_to_iso3()
    val = {}
    for _, r in ex.iterrows():
        iso = c2i.get(int(r["reporter_code"]))
        if iso:
            val[iso] = r["value_usd"]
    if not val:
        return ""
    vmax = np.log10(max(val.values()))
    vmin = np.log10(max(min(val.values()), 1e5))
    cmap = plt.get_cmap("YlGn")
    gj = json.loads(GEOJSON.read_text())
    fig, ax = plt.subplots(figsize=(9.4, 4.6))
    patches, colors = [], []
    for f in gj["features"]:
        iso = f["properties"].get("ISO_A3_EH") or f["properties"].get("ISO_A3")
        geom = f["geometry"]
        if not geom:
            continue
        polys = geom["coordinates"] if geom["type"] == "MultiPolygon" else [geom["coordinates"]]
        v = val.get(iso)
        col = cmap((np.log10(v) - vmin) / (vmax - vmin)) if v else "#ece9e4"
        for poly in polys:
            ring = np.array(poly[0])
            if ring.ndim == 2 and len(ring) >= 3:
                patches.append(MplPolygon(ring, closed=True))
                colors.append(col)
    pc = PatchCollection(patches, facecolor=colors, edgecolor="white", linewidth=0.25)
    ax.add_collection(pc)
    ax.set_xlim(-170, 180); ax.set_ylim(-58, 84)
    ax.set_aspect(1.25); ax.axis("off")
    return _png(fig)


